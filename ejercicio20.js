// Ejercicio 20: Calculadora de Interés Simple
let capital = 1000; // Definimos el capital inicial
let tasa = 5; // Definimos la tasa de interés en porcentaje
let tiempo = 3; // Definimos el tiempo en años

let interes = (capital * tasa * tiempo) / 100; // Fórmula del interés simple
console.log("Interés:", interes); // Mostramos el interés generado