// Ejercicio 5: Conversor de Temperatura
let celsius = 30; // Definimos la temperatura en grados Celsius
let fahrenheit = (celsius * 9/5) + 32; // Convertimos Celsius a Fahrenheit
console.log(celsius, "°C en Fahrenheit son:", fahrenheit, "°F"); // Mostramos conversión

let f = 86; // Definimos la temperatura en grados Fahrenheit
let c = (f - 32) * 5/9; // Convertimos Fahrenheit a Celsius
console.log(f, "°F en Celsius son:", c, "°C"); // Mostramos conversión